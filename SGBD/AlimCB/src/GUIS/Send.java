    private void doJob() {
        int cpt = 0;
        int cptErreur = 0;

        for(String id : listeId){
            Movies movie = couch.getMovie(id);
            oracle.initCall("Package_CB", "insertMovie", 21);
            int retour = oracle.insertMovie(movie, physique, digitale);
            if(retour == 1){
                cpt++;
                LnouveauFilm.setText(String.valueOf(cpt));
            }else if(retour == -1){
                cptErreur++;
                Lerreur.setText(String.valueOf(cptErreur));
            }
        }
    }

    private float[] distributionNormale(float mean, float ecartType) {
        Random rand = new Random();
        float theta = 2 * (float) Math.PI * rand.nextFloat();
        float rho = (float) Math.sqrt(-2 * Math.log(1 - rand.nextFloat()));
        float scale = ecartType * rho;

        float x = (float) (mean + scale * Math.cos(theta));
        float y = (float) (mean + scale * Math.sin(theta));
        if(x < 0) x = x * -1;
        if(y < 0) y = y * -1;
        float[] result = {x, y};
        return result;
    }
