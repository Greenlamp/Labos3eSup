    private void BenvoyerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BenvoyerActionPerformed
        String type = Gtype.getSelectedItem().toString();
        String message = Gmessage.getText();
        boolean toSend = false;

        if(type.equalsIgnoreCase("Question")){
            int id = new Random().nextInt(100000);
            message = "[" + this.login + "] Question id: " + id + " :\n" + message;
            toSend = true;
        }else if(type.equalsIgnoreCase("Réponse")){
            if(Gnumero.getItemCount() == 0){
                return;
            }
            int code = Integer.parseInt(Gnumero.getSelectedItem().toString());
            message = "[" + this.login + "] Reponse id: " + code + " :\n" + message;
            toSend = true;
        }else if(type.equalsIgnoreCase("Event")){
            message = "[" + this.login + "] EVENT\n" + message;
            toSend = true;
        }else{
            toSend = false;
        }

        if(toSend){
            envoyerMessageUdp(message);
            clearField();
        }
    }//GEN-LAST:event_BenvoyerActionPerformed

