    private void initUdp() {
        try {
            adresse = InetAddress.getByName(ipUdp);
            socketMulticast = new MulticastSocket(Integer.parseInt(portUdp));
            socketMulticast.joinGroup(adresse);
            ThreadReception reception = new ThreadReception(Gchat, socketMulticast, Gnumero);
            threadReception = new Thread(reception);
            threadReception.start();
            envoyerMessageUdp(login + " s'est connecte");
        } catch (UnknownHostException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void envoyerMessageUdp(String message) {
        try {
            DatagramPacket packet = new DatagramPacket(message.getBytes(), message.length(), adresse, Integer.parseInt(portUdp));
            socketMulticast.send(packet);
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void clearField() {
        Gmessage.setText("");
        Gtype.setSelectedIndex(0);
    }
