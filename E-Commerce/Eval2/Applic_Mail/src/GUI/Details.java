    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = GpiecesJointes.getSelectedValue().toString();
        for(PieceJointes pj : message.getPiecesJointes()){
            if(pj.getName().equalsIgnoreCase(name)){
                if(pj.isByte()){
                    pj.bytesToFile(name);
                }else{
                    pj.fileToBytes();
                    pj.bytesToFile(name);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

