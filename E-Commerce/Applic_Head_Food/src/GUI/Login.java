    private boolean verifLogin() {
        if(Glogin.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Veuillez entrez un login correct.");
            return false;
        }else if(Gpassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Veuillez entrez un password correct.");
            return false;
        }else{
            return true;
        }
    }

    private void TraiterPacketReceived(PacketCom packetReponse) {
        String type = packetReponse.getType();
        Object contenu = packetReponse.getObjet();
        if(type.equals(VSMEAP.LOGINHEAD_OK)){
            Main main = new Main(sock);
            main.show();
        }
    }
